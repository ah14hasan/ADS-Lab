// PROGRAM TO PRINT VARIABLES OF DIFFERENT DATA TYPES
import java.util.*;
import java.io.*;
class sample1 {
    public static void main(String[] args) {
        int x = 45;
        double d = 3.14;
        String s = "Hello, Java!";
        System.out.println("Integer: " + x);
        System.out.println("Double: " + d);
        System.out.println("String: " + s);
    }
}
